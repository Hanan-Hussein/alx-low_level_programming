#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**                                                                                                                                     
 * main - Entry point                                                                                                                   
 * Return:This returns lowercase alphabets                                                                                   
 */ 
int main(void)
{
int n;
for (n ='a'; n <= 'z'; n++)
{
putchar(n);
}
putchar('\n');
return (0);
}
